public class MinPoints {

    // השיטה הציבורית שמופיעה בחתימה הנדרשת בתרגיל.
    // היא רק "מפעילה" את הרקורסיה האמיתית, ומתחילה מהתא האחרון במערך
    // (mat.length-1, mat[0].length-1) - זה תא ההתחלה של המסלול.
    public static int minPoints(int[][] m) {
        return minPoints(m, m.length - 1, m[0].length - 1);
    }

    // שיטת עזר רקורסיבית (Overloading - אותו שם, פרמטרים שונים).
    // המשמעות של minPoints(m, i, j):
    // "מה המספר המינימלי (חיובי ממש) שצריך להיות בידיי בדיוק כשאני
    // עומד להיכנס לתא (i, j), כדי שאוכל להמשיך משם במסלול הכי טוב
    // שיש ולהגיע עד לתא (0,0), כאשר בכל ביקור - כולל בתא הזה
    // וכולל בתא (0,0) - הערך של number יישאר חיובי ממש?"
    private static int minPoints(int[][] m, int i, int j) {

        // --- מקרה בסיס: הגענו לתא הראשון (0,0), סוף המסלול ---
        // אחרי שנוסיף את m[0][0] ל-number, התוצאה חייבת להיות >= 1.
        // אז number לפני הכניסה חייב להיות >= 1 - m[0][0].
        // וגם number עצמו (לפני ההוספה) חייב להיות חיובי ממש,
        // ולכן לוקחים max(1, ...) כדי שלא נרד מתחת ל-1.
        if (i == 0 && j == 0) {
            return Math.max(1, 1 - m[0][0]);
        }

        // --- אנחנו בשורה הראשונה (i == 0) ו-j > 0 ---
        // מהתא (0, j) האפשרות היחידה היא לזוז שמאלה לתא (0, j-1),
        // אין אפשרות לזוז "מעלה" כי אנחנו כבר בשורה 0.
        // אז דורשים רקורסיבית כמה צריך להיכנס ל-(0, j-1),
        // ומחשבים מזה כמה צריך כבר להיות לפני הכניסה ל-(0, j)
        // (כלומר: לפני שמחסירים/מוסיפים את m[i][j]).
        if (i == 0) {
            return Math.max(1, minPoints(m, i, j - 1) - m[i][j]);
        }

        // --- אנחנו בעמודה הראשונה (j == 0) ו-i > 0 ---
        // מהתא (i, 0) האפשרות היחידה היא לזוז מעלה לתא (i-1, 0),
        // אין אפשרות לזוז שמאלה כי אנחנו כבר בעמודה 0.
        // בדיוק כמו למעלה, רק עם i-1 במקום j-1.
        if (j == 0) {
            return Math.max(1, minPoints(m, i - 1, j) - m[i][j]);
        }

        // --- המקרה הכללי: יש שתי אפשרויות תנועה ---
        // אפשר לזוז מ-(i, j) גם לתא (i-1, j) (שורה למעלה)
        // וגם לתא (i, j-1) (עמודה שמאלה).
        // בודקים רקורסיבית כמה number דרוש בכל אחד מהכיוונים,
        // ובוחרים את הכיוון "הזול" יותר (Math.min) - כי אנחנו
        // מחפשים את המסלול שדורש את ה-number ההתחלתי הכי קטן.
        int needFromUp = minPoints(m, i - 1, j); // דרישה אם נמשיך מעלה
        int needFromLeft = minPoints(m, i, j - 1); // דרישה אם נמשיך שמאלה

        // לוקחים את המסלול הזול מבין השניים, ומחשבים כמה number
        // צריך להיות *לפני* הכניסה לתא (i, j) - כלומר לפני שמחסירים/
        // מוסיפים את m[i][j] - כדי שאחרי ההוספה עדיין נספיק לכל מה שנדרש
        // בהמשך המסלול. גם כאן, לא פחות מ-1 (חיובי ממש).
        return Math.max(1, Math.min(needFromUp, needFromLeft) - m[i][j]);
    }

    // בדיקה מהירה על הדוגמה מהתרגיל
    public static void main(String[] args) {
        int[][] mat = {
                { -2, -3, 3 },
                { -5, -10, 1 },
                { 10, 30, -5 }
        };
        System.out.println("minPoints = " + minPoints(mat)); // ידפיס 6
    }
}